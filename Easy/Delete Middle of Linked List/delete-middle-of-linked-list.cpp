// Deletes middle of linked list and returns head of the modified list
class Solution{
    public:
    Node* deleteMid(Node* head)
    {
        if(!head || !head->next)
        {
            return nullptr;
        }
        // Your Code Here
        int cnt = 0;
        
        struct Node* ptr = head;
        
        while(ptr != nullptr)
        {
            cnt++;
            ptr = ptr->next;
        }
        
        // cout<<"cnt ="<<cnt<<endl;
        
        int mid = (cnt/2) - 1;
        
        // cout<<"mid ="<<mid<<endl;
        
        ptr = head;
        
        while(mid--)
        {
            ptr = ptr->next;
        }
        
        ptr->next = ptr->next->next;
        
        return head;
        
    }
};

